def readable_stem(page: int, title: str) -> str:
    """生成排序稳定、人在文件树里也能直接读懂的文件名。"""
    topic = re.sub(r"^\d+-\d+\s*", "", title).strip()
    chapter_title = chapter_for(page)[3]
    if topic in {"本章简介", "本章介绍"}:
        topic = f"{chapter_title}-本章导学"
    elif topic == "本章总结":
        topic = f"{chapter_title}-本章总结"
    topic = re.sub(r"[^\w\u4e00-\u9fff]+", "-", topic, flags=re.UNICODE)
    topic = re.sub(r"-+", "-", topic).strip("-_")
    return f"p{page:03d}-{topic[:48]}"


