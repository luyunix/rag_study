def svg_lines(
    text: str, x: int, y: int, class_name: str, width: int = 11, gap: int = 24
) -> str:
    chunks = []
    chunk = ""
    units = 0
    max_units = width * 2
    for character in text:
        character_units = (
            2 if unicodedata.east_asian_width(character) in {"W", "F", "A"} else 1
        )
        if chunk and units + character_units > max_units:
            chunks.append(chunk.strip())
            chunk = ""
            units = 0
        chunk += character
        units += character_units
    if chunk:
        chunks.append(chunk.strip())
    return "".join(
        f'<text x="{x}" y="{y + index * gap}" text-anchor="middle" '
        f'class="{class_name}">{escape(chunk)}</text>'
        for index, chunk in enumerate(chunks)
    )


