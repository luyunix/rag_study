        page_path = directory / f"{entry['stem']}.md"
        if entry["page"] not in HANDCRAFTED_PAGES:
            page_path.write_text(
                render_section(
                    entry,
                    entries[index - 1] if index else None,
                    entries[index + 1] if index + 1 < len(entries) else None,
                ),
                encoding="utf-8",
            )
        _, _, directory_name, chapter_title = chapter_for(entry["page"])
        if entry["page"] not in HANDCRAFTED_PAGES:
            (diagram_dir / f"{entry['stem']}-concept.svg").write_text(
                render_diagram(entry, directory_name, chapter_title),
                encoding="utf-8",
            )
        transcript_path = transcript_dir / f"p{entry['page']:03d}.json"
        if transcript_path.exists():
            transcript = json.loads(transcript_path.read_text(encoding="utf-8"))
            (transcript_output_dir / f"{entry['stem']}-ASR.md").write_text(
                render_transcript(entry, transcript),
                encoding="utf-8",
            )

